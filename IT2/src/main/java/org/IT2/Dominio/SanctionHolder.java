public class SanctionHolder {
	private Object _id;
	private Object _dni;
	private Object _name;
	private Object _lastName;
	private Object _fullAddress;

	public void getId() {
		return this._id;
	}

	public void setId(Object aId) {
		this._id = aId;
	}

	public void getDni() {
		return this._dni;
	}

	public void setDni(Object aDni) {
		this._dni = aDni;
	}

	public void getName() {
		return this._name;
	}

	public void setName(Object aName) {
		this._name = aName;
	}

	public void getLastName() {
		return this._lastName;
	}

	public void setLastName(Object aLastName) {
		this._lastName = aLastName;
	}

	public void getFullAddress() {
		return this._fullAddress;
	}

	public void setFullAddress(Object aFullAddress) {
		this._fullAddress = aFullAddress;
	}
}
